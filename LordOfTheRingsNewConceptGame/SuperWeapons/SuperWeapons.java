package LordOfTheRingsNewConceptGame.SuperWeapons;

public class SuperWeapons {
    private String name;
    private double makeDamage;
    private double takeDamage;
    private double weaponExp;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMakeDamage() {
        return makeDamage;
    }

    public void setMakeDamage(double makeDamage) {
        this.makeDamage = makeDamage;
    }

    public double getTakeDamage() {
        return takeDamage;
    }

    public void setTakeDamage(double takeDamage) {
        this.takeDamage = takeDamage;
    }

    public double getWeaponExp() {
        return weaponExp;
    }

    public void setWeaponExp(double weaponExp) {
        this.weaponExp = weaponExp;
    }



}
